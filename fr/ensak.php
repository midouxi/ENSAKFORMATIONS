        <div>
          <p> &nbsp;&nbsp;   Faisant partie du réseau ENSA du Maroc, l’Ecole Nationale des Sciences Appliquées de Khouribga relève de l’Université Hassan 1er de Settat. Elle a été créée en 2007 pour appuyer la volonté gouvernementale dans le cadre de l’initiative nationale de formation de 10 000 ingénieurs à l’horizon 2010. Elle forme des ingénieurs d’Etat hautement qualifiés au niveau scientifique, technique, modélisation, management et communication.</p>
          <p> &nbsp;&nbsp; L’école dispense un cursus de 5 ans d’études : un cycle ingénieur en 3 années (6 semestres) débouchant sur un diplôme d’Ingénieur d’Etat précédé par des années préparatoires intégrées qui durent 2 ans (4 semestres).</p>
          <ul>
            <li><strong> &nbsp;&nbsp; Le cycle préparatoire intégré </strong>permet à l’élève ingénieur d’acquérir des connaissances solides en mathématiques et physique ainsi qu’une initiation à l’informatique.</li>
            <li><strong> &nbsp;&nbsp; Le cycle ingénieur</strong> qui dure 3 ans est accessible directement après deux années des classes préparatoires intégrées et aux admis au CNC des grandes écoles et par voie de concours au BAC+2/3 de nature scientifique ou technique dans la limite des places disponibles. C’est à partir de ce stade que l’élève ingénieur s’ouvre sur le monde industriel. En tronc commun, les études se concentrent sur les bases de l’ingénierie à savoir la modélisation mathématique, l’informatique, l’électronique et les signaux…etc. à partir de la 4ème année, l’élève ingénieur de l’ENSAK, se spécialise dans des domaines très demandés en entreprises à savoir le génie des réseaux et télécommunications, le génie électrique (électronique des systèmes embarqués et commande numérique), Génie des procédés de l'énergie et de l'environnement et le génie informatique (ingénierie logiciel). D’autres filières de formations vont êtres mises en place prochainement. Les études sont perfectionnées par des stages, mini projets et visites d’entreprises, conférences…etc.</li>
          </ul>
          <p>  &nbsp;&nbsp;A côté de l’encadrement scientifique et technique, le corps administratif et enseignant de l’ENSAK a pour mission de favoriser l’ouverture de l’établissement sur son environnement socio-économique et de compléter harmonieusement la formation des étudiants<p> 
